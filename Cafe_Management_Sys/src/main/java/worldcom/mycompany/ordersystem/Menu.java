    public void getTax(int t){
    if(t>=1&&t<=200){
        tax = 30.0;
    }
    else if(t>200&&t<=500){
        tax = 60;
    }
    else if(t>500&&t<=1000){
        tax = 100;
    }
    else if(t>1000&&t<=2000){
        tax = 150;
    }
     else if(t>2000&&t<=3000){
        tax = 200;
    }
    else if(t>3000&&t<=4000){
        tax = 250;
    }
    else if(t>4000&&t<=5000){
        tax = 300;
    }
    else if(t>5000){
        tax=450;
    }
    
}
