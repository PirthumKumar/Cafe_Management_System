public void actionPerformed(ActionEvent e) {
                try {
                    // Get the given amount from jTextField1
                    double givenAmount = Double.parseDouble(jTextField1.getText());

                    // Calculate the return payment
                    double returnPayment = givenAmount - total;

                    // Set the result in jTextField2
                    jTextField2.setText(String.valueOf(returnPayment));
                } catch (NumberFormatException ex) {
                    Component frame = null;
                    // Handle the exception if the input is not a valid number
                    JOptionPane.showMessageDialog(frame, "Please enter a valid number", "Error", JOptionPane.ERROR_MESSAGE);
                }
}
